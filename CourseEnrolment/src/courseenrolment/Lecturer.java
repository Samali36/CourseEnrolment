package courseenrolment;

/**
 *
 * @author wathsara
 */
public class Lecturer {
    private int lecID;
    private String faculty;
    private String type;
    private String name;
    private String email;
    private String dob;
    private String cno;
    private String address;
    private String qualification;
    private String institution;
    private String graYear;
    private String gender;
    private byte[] Pic;

    /**
     * @return the faculty
     */
    public String getFaculty() {
        return faculty;
    }

    /**
     * @param faculty the faculty to set
     */
    public void setFaculty(String faculty) {
        this.faculty = faculty;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the dob
     */
    public String getDob() {
        return dob;
    }

    /**
     * @param dob the dob to set
     */
    public void setDob(String dob) {
        this.dob = dob;
    }

    /**
     * @return the cno
     */
    public String getCno() {
        return cno;
    }

    /**
     * @param cno the cno to set
     */
    public void setCno(String cno) {
        this.cno = cno;
    }

    /**
     * @return the address
     */
    public String getAddress() {
        return address;
    }

    /**
     * @param address the address to set
     */
    public void setAddress(String address) {
        this.address = address;
    }

    /**
     * @return the qualification
     */
    public String getQualification() {
        return qualification;
    }

    /**
     * @param qualification the qualification to set
     */
    public void setQualification(String qualification) {
        this.qualification = qualification;
    }

    /**
     * @return the institution
     */
    public String getInstitution() {
        return institution;
    }

    /**
     * @param institution the institution to set
     */
    public void setInstitution(String institution) {
        this.institution = institution;
    }

    /**
     * @return the graYear
     */
    public String getGraYear() {
        return graYear;
    }

    /**
     * @param graYear the graYear to set
     */
    public void setGraYear(String graYear) {
        this.graYear = graYear;
    }

    /**
     * @return the gender
     */
    public String getGender() {
        return gender;
    }

    /**
     * @param gender the gender to set
     */
    public void setGender(String gender) {
        this.gender = gender;
    }

    /**
     * @return the Pic
     */
    public byte[] getPic() {
        return Pic;
    }

    /**
     * @param Pic the Pic to set
     */
    public void setPic(byte[] Pic) {
        this.Pic = Pic;
    }

    /**
     * @return the lecID
     */
    public int getLecID() {
        return lecID;
    }

    /**
     * @param lecID the lecID to set
     */
    public void setLecID(int lecID) {
        this.lecID = lecID;
    }

}
