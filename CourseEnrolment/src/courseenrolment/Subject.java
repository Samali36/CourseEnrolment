package courseenrolment;

/**
 *
 * @author wathsara
 */
public class Subject {
    private String faculty;
    private String type;
    private String semester;
    private String academicYear;
    private String subType;
    private String subCode;
    private String subName;
    private int subFee;
    private int credit;
    private String lecturerIncharge;

    /**
     * @return the faculty
     */
    public String getFaculty() {
        return faculty;
    }

    /**
     * @param faculty the faculty to set
     */
    public void setFaculty(String faculty) {
        this.faculty = faculty;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the semester
     */
    public String getSemester() {
        return semester;
    }

    /**
     * @param semester the semester to set
     */
    public void setSemester(String semester) {
        this.semester = semester;
    }

    /**
     * @return the academicYear
     */
    public String getAcademicYear() {
        return academicYear;
    }

    /**
     * @param academicYear the academicYear to set
     */
    public void setAcademicYear(String academicYear) {
        this.academicYear = academicYear;
    }

    /**
     * @return the subType
     */
    public String getSubType() {
        return subType;
    }

    /**
     * @param subType the subType to set
     */
    public void setSubType(String subType) {
        this.subType = subType;
    }

    /**
     * @return the subCode
     */
    public String getSubCode() {
        return subCode;
    }

    /**
     * @param subCode the subCode to set
     */
    public void setSubCode(String subCode) {
        this.subCode = subCode;
    }

    /**
     * @return the subName
     */
    public String getSubName() {
        return subName;
    }

    /**
     * @param subName the subName to set
     */
    public void setSubName(String subName) {
        this.subName = subName;
    }

    /**
     * @return the subFee
     */
    public int getSubFee() {
        return subFee;
    }

    /**
     * @param subFee the subFee to set
     */
    public void setSubFee(int subFee) {
        this.subFee = subFee;
    }

    /**
     * @return the credit
     */
    public int getCredit() {
        return credit;
    }

    /**
     * @param credit the credit to set
     */
    public void setCredit(int credit) {
        this.credit = credit;
    }

    /**
     * @return the lecturerIncharge
     */
    public String getLecturerIncharge() {
        return lecturerIncharge;
    }

    /**
     * @param lecturerIncharge the lecturerIncharge to set
     */
    public void setLecturerIncharge(String lecturerIncharge) {
        this.lecturerIncharge = lecturerIncharge;
    }
    
    
}
